public class ternary_operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int max = (a > b ? a : b); // ternary operator apply here
        int max2 = (a < b ? a : b); // ternary operator apply here
        System.out.println(max);
        System.out.println(max2);
    }
}
