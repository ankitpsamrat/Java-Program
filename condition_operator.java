public class condition_operator {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        System.out.println(a < b); // less than condition
        System.out.println(a <= b); // less than equal to condition
        System.out.println(a > b); // greater than condition
        System.out.println(a >= b); // greater than equal to condition
        System.out.println(a == b); // equal to condition
        System.out.println(a != b); // not equal to condition
    }
}
