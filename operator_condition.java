public class operator_condition {

    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        // less than
        System.out.println(a < b);

        // less than equal to
        System.out.println(a <= b);

        // greater than
        System.out.println(a > b);

        // greater than equal to
        System.out.println(a >= b);

        // equal to
        System.out.println(a == b);

        // not equal to
        System.out.println(a != b);
    }
}
